/**
 * Search icons by name or synonym. Returns matching icon names.
 * Empty query returns all icons.
 */
export function searchIcons(query: string): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return ICON_NAMES;

  const matches = new Set<string>();

  // Match icon names (e.g. "star" matches "Star", "flash" matches "FlashOn")
  for (const name of ICON_NAMES) {
    if (name.toLowerCase().includes(q)) {
      matches.add(name);
    }
  }

  // Match synonyms
  for (const [synonym, icons] of Object.entries(ICON_SYNONYMS)) {
    if (synonym.includes(q)) {
      for (const icon of icons) {
        matches.add(icon);
      }
    }
  }

  return Array.from(matches);
}
